# numpy.dot（a，b，out = None) 两个数组的点积
import numpy as np

print(np.dot(4, 5))  # 结果输出：20

print(np.dot([4], [5]))  # 结果输出：20

print(np.dot([2, 3], [4, 5]))
# 结果输出：23
# 计算过程：2*4+3*5=23

